    private void btnDlt2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDlt2ActionPerformed
        int selectedRow = entJTbl.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "To view details, please first select a row from the table.", "Warning!", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            En enterprise = (En) entJTbl.getValueAt(selectedRow, 0);
            int dialogResult = JOptionPane.showConfirmDialog(null, "Do you wish to continue?");
            if (dialogResult == JOptionPane.YES_OPTION) {
                Network network = (Network) entJTbl.getValueAt(selectedRow, 1);
                network.getEntDir().getEntList().remove(enterprise);
                popTbl();
                JOptionPane.showMessageDialog(null, "Enterprise is deleted");
            }

        }

    }//GEN-LAST:event_btnDlt2ActionPerformed

