    private void submitJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJBtnActionPerformed

        Network network = (Network) netwrkJComboBox.getSelectedItem();
        En.EntType type = (En.EntType) entTypeJComboBox.getSelectedItem();

        if (network == null || type == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        } else {
            String enterpriseName = nameJTextField.getText();
            List<En> enterprises = network.getEntDir().getEntList();
            List<String> names = new ArrayList<>();
            for (En enterprise : enterprises) {
                names.add(enterprise.getName());
            }
            if (names.contains(enterpriseName)) {
                JOptionPane.showMessageDialog(null, "Enterprise Altready exists!!", "Warning!", JOptionPane.WARNING_MESSAGE);
                return;
            } else {
                if (enterpriseName.equals("") || enterpriseName == null) {
                    JOptionPane.showMessageDialog(null, "Please enter Name");
                    return;
                } else {
                    En enterprise = network.getEntDir().crtAndAddEnt(enterpriseName, type);

                    popTbl();
                    JOptionPane.showMessageDialog(null, "Enterprise Added Successfully!!");
                    nameJTextField.setText("");
                }
            }
        }

    }//GEN-LAST:event_submitJBtnActionPerformed

